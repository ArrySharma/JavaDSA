import java.util.*;

public class switchStatement {
    public static void main(String args[]){

        int number = 2;
        switch(number){
            case 1 : System.out.println("case 1 excecuted");
            break;
            case 2 : System.out.println("case 2 excecuted");
            break;
            case 3 : System.out.println("case 3 excecuted");
            break;
            default : System.out.println("case default excecuted");
            break;
            
        }

    }
    }